	-- Get the maximum comment count across all posts
	SELECT MAX(commentCount)
	FROM (
		-- Get the number of comments for each post
		SELECT COUNT(originalPostID) as commentCount
		FROM postreply
		GROUP BY originalPostID
	) a -- Pseudo table name
